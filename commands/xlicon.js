cmd({
            pattern: "AUD",
            react: "ΏθΦΛ",
            alias :[],
            desc: "Send YouTube link",
            category: "downloader",
            filename: __filename,
            use: '<text>',
        },
        async(Void, citel, text) => {
		try{
var msg = citel	
if(!msg.quoted) return 
if (!msg.quoted.isBaileys ) return 
if(!msg.quoted.caption) return console.log('ew')
text = msg.quoted.caption
if (!text.includes('ΏθΟπ ΏζΡΨΏζΡΑΏζΡΜΏζΡΥΏζΡΕΏζΡΣ-ΏζΡΩ ΏζΩοΏζΩλΏζΩκΏζΩγ ΏζΩΩΏζΩλΏζΩςΏζΩκΏζΩθΏζΩλΏζΩΩΏζΩαΏζΩξΏθΟπ'))  return 
text = text.split('έΧΠΏθΥκ *Url* : ')[1].split('\n')[0]		
if(!text) return 
await Void.sendMessage(citel.chat, { react: {  text: "ΏθΟπ", key: msg.key } } )			

		        const getRandom = (ext) => {
            return `${Math.floor(Math.random() * 10000)}${ext}`;
        };

        if (text.length === 0) {
            citel.reply(`έζΝ URL is empty! \nSend ${prefix}ytmp3 url`);
            return;
        }
            let urlYt = text;
            if (!urlYt.startsWith("http")) {
                citel.reply(`έζΝ Give youtube link!`);
                return;
            }
            let infoYt = await ytdl.getInfo(urlYt);
            if (infoYt.videoDetails.lengthSeconds >= videotime) {
                citel.reply(`έζΝ I can't download that long video!`);
                return;
            }
            let titleYt = infoYt.videoDetails.title;
            let randomName = `${Math.floor(Math.random() * 10000)}.mp3`;

            const stream = ytdl(urlYt, {
                    filter: (info) => info.audioBitrate == 160 || info.audioBitrate == 128,
                })
                .pipe(fs.createWriteStream(`./${randomName}`));

            await new Promise((resolve, reject) => {
                stream.on("error", reject);
                stream.on("finish", resolve);
            });

            let stats = fs.statSync(`./${randomName}`);
            let fileSizeInMegabytes = stats.size / (1024 * 1024);

            if (fileSizeInMegabytes <= dlsize) {
                let buttonMessage = {
                    audio: fs.readFileSync(`./${randomName}`),
                    mimetype: 'audio/mpeg',
                    fileName: titleYt + ".mp3",
                }

                await Void.sendMessage(citel.chat, buttonMessage, { quoted: citel })
                fs.unlinkSync(`./${randomName}`);
            }

		}catch(e){
			citel.reply('' + e)
		}
})


